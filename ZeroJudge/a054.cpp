#include<stdio.h>
main() {
    char id[27][3] =     {
    "10","11","12","13","14",
    "15","16","17","34","18",
    "19","20","21","22","35",
    "23","24","25","26","27",
    "28","29","32","30","31",
    "33"};
    char s[10];
    while(scanf("%s", s) == 1) {
        int sum = 0, a;
        for(a = 0; a < 8; a++)
            sum += (s[a]-48) * (8-a);
        s[8] = (s[8] == '0') ? 10 + '0': s[8];
        for(a = 0; a < 26; a++)
            if(10 - (sum + id[a][0] - '0' + (id[a][1] - '0')*9)%10 == (s[8] - '0'))
                printf("%c", a+'A');
        puts("");
    }
    return 0;
}